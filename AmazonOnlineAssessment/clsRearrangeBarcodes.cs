        public int[] RearrangeBarcodes(int[] barcodes)
        {
            Dictionary<int, int> hashMap = new Dictionary<int, int>();

            foreach (var barcode in barcodes)
            {
                if (hashMap.ContainsKey(barcode))
                {
                    hashMap[barcode] += 1;
                }
                else
                {
                    hashMap[barcode] = 1;
                }
            }

            // Max Heap
            Heap<HeapItem> heap = new Heap<HeapItem>();

            foreach (var itm in hashMap)
            {
                heap.Enqueue(new HeapItem(itm.Key, itm.Value));
            }

            List<int> lstOutput = new List<int>();

            while (heap.Size > 0)
            {
                int k = 2;
                List<HeapItem> processedItem = new List<HeapItem>();

                heap.Heapify();

                while (k > 0 && heap.Size > 0)
                {
                    HeapItem current = heap.Dequeue();
                    heap.Heapify();
                    lstOutput.Add((int)current.Value);
                    current.Priority -= 1;
                    processedItem.Add(current);
                    k--;
                }

                foreach (var queueItem in processedItem)
                {
                    if (queueItem.Priority > 0)
                    {
                        heap.Enqueue(queueItem);
                    }
                }
            }

            return lstOutput.ToArray();
        }
